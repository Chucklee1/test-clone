# include <iostream>
using namespace std;

int main() {

  char sym;
  float num1, num2;

  cout << "Enter two numbers: ";
  cin >> num1 >> num2;

  cout << "Which operation do you want: +, -, *, /: ";
  cin >> sym;

  switch(sym) {

    case '+':
      cout << num1 << " + " << num2 << " = " << num1 + num2;
      break;

    case '-':
      cout << num1 << " - " << num2 << " = " << num1 - num2;
      break;

    case '*':
      cout << num1 << " * " << num2 << " = " << num1 * num2;
      break;

    case '/':
      cout << num1 << " / " << num2 << " = " << num1 / num2;
      break;

    default:
      // If the operator is other than +, -, * or /, error message is shown
      cout << "Error! operator is not correct";
      break;
  }

  return 0;
}
